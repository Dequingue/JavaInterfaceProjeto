    private void Logar(){
     try{
         
            //Capturando dados dos campos
            String email = txtEmail.getText();
            String senha = txtSenha.getText();
        
            // Criando objeto "UsuarioDTO para armazenar as credenciais do usuario"
            UsuariosDTO objusuariodto = new UsuariosDTO();
            objusuariodto.setNome_usuario(email);
            objusuariodto.setSenha_usuario(senha);
            
            // Utilizando a classe "UsuariDAO" para verificar se as credenciais do usuario est√£o corretas
            UsuariosDAO objUsuariosdao =  new UsuariosDAO();
           
            UsuariosDTO usuario = objUsuariosdao.autenticarUsuario(email, senha);
            
            // Verificando se o login do usuario esta completo
            if(usuario != null){
                 //  Define a sess√£o do usu√°rio
                SessaoUsuario.setIdUsuario(usuario.getId_Usuarios());
                
                home2 principal = new home2();
                principal.setVisible(true);
                dispose();
            }else{
                //enviar mensagem incorreta
                JOptionPane.showMessageDialog(null, "Usuario ou senha invalidos");
                
            }
         
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,"View Login erro"+e);
        } 

    }
    
