    private void cadastro(){
        try{
            String nome = txtEmail.getText();
            String senha = txtSenha.getText();
        
            CadastroDTO objcadastrodto = new CadastroDTO();
            objcadastrodto.setNome_cadastrar(nome);
            objcadastrodto.setSenha_cadastrar(senha);
        
            CadastroDAO objcadastrodao = new CadastroDAO();
            objcadastrodao.cadastrando(objcadastrodto);
            
            JOptionPane.showMessageDialog(null, "Cadastro realizado com sucesso");
            
            TelaLogin lg = new TelaLogin();
            lg.setVisible(true);
            dispose();
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,"Erro Cadastro não realizado"+e);
        }

        
        
    }
